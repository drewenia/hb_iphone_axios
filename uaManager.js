function getRandomBrowserConfig() {
    if (UA_CONFIGS.length === 0) {
        // Liste boşsa varsayılan güvenli konfigürasyonu döndür
        return {
            userAgent: "Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/125.0.0.0 Safari/537.36",
        };
    }

    const randomIndex = Math.floor(Math.random() * UA_CONFIGS.length);

    // selectedUA artık doğrudan User-Agent dizesidir
    const selectedUA = UA_CONFIGS[randomIndex];

    // SADECE selectedUA dizesini döndürüyoruz.
    return {
        userAgent: selectedUA // Dizeyi doğrudan kullan
    };
}

function getRandomAcceptLanguage() {
    const randomIndex = Math.floor(Math.random() * ACCEPT_LANGUAGES.length);
    return ACCEPT_LANGUAGES[randomIndex];
}

